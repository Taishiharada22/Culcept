// 煽りではなく「あ、見られてる」と感じさせる精度感
// 各予測は複数段落で具体的に。1文で終わらせず、行動の奥にある動機まで言い当てる

// ---------------------------------------------------------------------------
// Types
// ---------------------------------------------------------------------------

/** オンボーディング回答（-1.0〜+1.0 のスケール） */
export interface OnboardingAnswers {
  /** Q7: 計画 vs 即興 */
  q7_plan_vs_spontaneous?: number;
  /** Q9: 好奇心の方向 */
  q9_curiosity_direction?: number;
  /** Q10: 金曜の夜の誘い — 社交スタイル */
  q10_social_style: number;
  /** Q11: 友人との意見衝突 — 対人葛藤スタイル */
  q11_conflict_style: number;
  /** Q13: 迷ったときの判断 — 選択スタイル */
  q13_decision_style: number;
  /** Q14: 締め切りの向き合い方 */
  q14_deadline_style?: number;
  /** Q16: 大人数の中での振る舞い */
  q16_crowd_behavior?: number;
  /** Q18: 情報の受け取り方 */
  q18_info_processing?: number;
}

/** クラスタ ID */
export type BehaviorClusterId =
  | "guardian"      // 慎重で調和重視、計画的
  | "adventurer"    // 大胆で直感的、即興的
  | "analyst"       // 論理的で独立的、熟考型
  | "harmonizer"    // 共感的で関係重視、柔軟
  | "challenger"    // 率直で行動的、決断が速い
  | "observer";     // 距離を取って観察、内省的

/** 予測シーン */
export type PredictionScene =
  | "party"         // 飲み会・社交場面
  | "shopping"      // 買い物・意思決定場面
  | "conflict";     // 対人衝突・摩擦場面

/** 1つの行動予測 */
export interface BehavioralPrediction {
  scene: PredictionScene;
  /** カジュアルで見透かすような予測テキスト */
  text: string;
  /** 確信度 0-1 */
  confidence: number;
}

/** クラスタ分類結果 */
export interface ClusterResult {
  clusterId: BehaviorClusterId;
  clusterLabel: string;
  predictions: BehavioralPrediction[];
  /** 二次クラスタ（混合型の場合） */
  secondaryClusterId?: BehaviorClusterId;
}

