function distanceToCluster(
  answers: OnboardingAnswers,
  cluster: ClusterDef,
): number {
  const ranges: Array<{ value: number; range: [number, number] }> = [
    { value: answers.q10_social_style, range: cluster.socialRange },
    { value: answers.q11_conflict_style, range: cluster.conflictRange },
    { value: answers.q13_decision_style, range: cluster.decisionRange },
  ];

  let totalDist = 0;
  for (const { value, range } of ranges) {
    const mid = (range[0] + range[1]) / 2;
    const halfWidth = (range[1] - range[0]) / 2;
    const normalized = Math.abs(value - mid) / Math.max(halfWidth, 0.01);
    totalDist += normalized * normalized; // 二乗距離
  }
  return Math.sqrt(totalDist / ranges.length);
}

/** クラスタ分類 + 予測生成 */
export function classifyAndPredict(
  answers: OnboardingAnswers,
): ClusterResult {
  // 全クラスタとの距離を計算
  const scored = CLUSTERS.map((c) => ({
    cluster: c,
    distance: distanceToCluster(answers, c),
  })).sort((a, b) => a.distance - b.distance);

  const best = scored[0];
  const second = scored[1];

  // 信頼度: 最近接クラスタとの距離が小さいほど高い
  const baseConfidence = Math.max(0, 1 - best.distance * 0.5);

  // 各シーンから1つずつテンプレートを選択（回答値でシード）
  const scenes: PredictionScene[] = ["party", "shopping", "conflict"];
  const predictions: BehavioralPrediction[] = scenes.map((scene, i) => {
    const templates = TEMPLATES[best.cluster.id][scene];
    // 回答の組み合わせで擬似ランダム選択（同じ回答なら同じ結果）
    const seed = Math.abs(
      answers.q10_social_style * 1000 +
      answers.q11_conflict_style * 100 +
      answers.q13_decision_style * 10 +
      i,
    );
    const idx = Math.floor(seed) % templates.length;
    return {
      scene,
      text: templates[idx],
      confidence: Math.min(1, baseConfidence + (i === 0 ? 0.05 : 0)),
    };
  });

  return {
    clusterId: best.cluster.id,
    clusterLabel: best.cluster.label,
    predictions,
    secondaryClusterId:
      second.distance - best.distance < 0.3
        ? second.cluster.id
        : undefined,
  };
}

