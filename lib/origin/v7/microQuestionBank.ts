/* ─── Helper ─── */

type OptionTuple = [id: string, label: string, icon: string];

function q(
  id: string,
  question: string,
  lifePeriod: LifePeriod,
  category: MicroQuestionCategory,
  opts: OptionTuple[],
  allowFreeText = true,
): MicroQuestion {
  const options: MicroQuestionOption[] = opts.map(([oid, label, icon]) => ({
    id: oid,
    label,
    icon,
  }));
  return { id, question, lifePeriod, category, options, allowFreeText };
}

